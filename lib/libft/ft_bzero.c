#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	t_byte	*s_ptr;

	i = 0;
	s_ptr = (t_byte *)s;
	while (i < n)
	{
		s_ptr[i] = 0;
		i++;
	}
}
